      <h1>Lunar Halo Study</h1>
      <p class="note-lead">A quiet session focused on the geometry of a pale 22-degree halo.</p>

      <div class="note-content">
        <p>At 21:18 the Moon sat high in the southeast with thin cirrostratus spreading from the west. A clear halo formed with a faint outer ring. The inner ring held steady at approximately 22 degrees, while a wider 44-degree circle came and went with the cloud density.</p>
        <p>Recorded brightness levels every five minutes. The brightest arc was at the top of the ring, showing mild color separation with a bluish inner edge and a subtle warm tint on the outside. Star field contrast reduced by about half compared to the previous night.</p>
        <p>By 22:05 the halo faded as the cirrostratus thinned. The sky returned to darker background levels, with Orion's belt visible and crisp again.</p>
